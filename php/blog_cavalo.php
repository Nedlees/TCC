  <h1>Cavalos relincham: entenda melhor</h1>

  <p class="texto">
  Seu cavalo tem um relincho que é único e distinto. Dentro de um rebanho doméstico ou selvagem, os cavalos usam esse som para chamar uns aos outros. Como resultados, podem até usá-lo para chamar a atenção de uma pessoa. Eventualmente, estão dizendo: 'Estou aqui, onde você está'; ou: 'Ei, amigo, preste atenção em mim'.
<br><br>
Dependendo do tom e da linguagem corporal, um relincho pode ser usado quando o cavalo está ansioso ou confiante. Entretanto, às vezes, eles podem gritar por você porque estão com fome e impacientes. Em outros momentos, os cavalos relincham porque sentem falta de seus companheiros de rebanho. Observe suas orelhas, olhos e cauda com o intuito de entender por que estão vocalizando.
  </p>

