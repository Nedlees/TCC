  <h1>Outros Sons</h1>
  
  <li class="lista">
  Há sons mais altos que outros quando falamos de relincho. Os cavalos geralmente estão tentando chamar a atenção de outro companheiro de rebanho ou pessoa.
  </li>
  <li class="lista">
  Seu cavalo também pode guinchar, especialmente as éguas. Muitas vezes, os cavalos guincham na primeira vez que se encontram. Em algumas ocasiões, isso pode sugerir que o cavalo está excitado ou muito animado.
  </li>
  <li class="lista">
  Um bufo alto do nariz do seu cavalo geralmente significa que ele está alarmado. Você deve se preparar para eles se assustarem.
  </li>
  <li class="lista">
  Alguns cavalos também suspirarão. Na verdade, é um sinal de relaxamento e alívio. No entanto, também pode significar tédio.
  </li>

