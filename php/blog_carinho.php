    <h1>Por que os cães lambem as pessoas e outros animais?</h1>
<br>
    <p class="texto">Para responder a essa pergunta é preciso entender algumas características do cachorro. Quando em
      estado selvagem — e o mesmo ocorre em relação aos lobos —, os cães possuem o hábito de lamber uns aos outros.
      <br><br>
      Os motivos são diversos e estão intimamente relacionados à hierarquia da matilha e como os seus integrantes se
      relacionam entre si. E mais, o hábito de lamber nunca é visto como algo ruim! Confira as razões e comece a
      descobrir por que o cachorro lambe o dono:
    </p>
    <li class="lista">
      para pedir comida: os filhotes costumam lamber a boca e o queixo da mãe quando querem comer. Essas lambidas
      estimulam a cachorra a regurgitar seu alimento para os filhotes;
    </li>
    <li class="lista">
      para amenizar situações de conflito.
    </li>
    <li class="lista">
      para identificar e manter o cheiro dos componentes da matilha: principalmente quando algum membro retorna para
      casa, os cães podem lambê-lo para que o cheiro da matilha. Assim, eles mantêm a unidade do grupo;
    </li>
    <li class="lista">
      como reverência: os cães podem lamber aqueles aos quais ele tem adoração. É comum também se esfregarem no rosto ou
      orelhas desses animais;
    </li>
    <li class="lista">
      para saber por onde o companheiro andou: no retorno para o lar, o cão que recebe o outro o lambe para sentir o
      gostinho de onde o parceiro esteve. É uma forma de obter muitas informações: se houve algum conflito, se outro
      animal está doente, etc.;
    </li>
