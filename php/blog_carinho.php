<h1>Faz mal deixar o cachorro lamber?</h1>

<p class="texto">Uma das grandes preocupações das pessoas, principalmente quando se trata de crianças, é se as lambidas dos cães podem transmitir alguma doença. Do ponto de vista afetivo, receber lambidas de cachorros pode ser uma delícia. Já do ponto de vista da medicina, o consenso é de que bem não faz.
<br><br>
De acordo com alguns médicos, como o bacteriologista John Oxford, da Universidade de Londres, e o Dr. Leni K. Kaplan, da Faculdade de Medicina da Universidade Cornell, nos Estados Unidos, a lambida de cachorro pode carregar microrganismos nocivos à nossa saúde.
<br><br>
É o caso, por exemplo, da Escherichia coli, da salmonela, da giardia e da campylobacter, responsável por quadros de gastroenterite, com sinais como vômitos, diarreia, flatulência e dor abdominal. Quem já teve giardíase sabe o quanto se passa mal!</p>

<h1>Como tornar as lambidas mais “seguras”</h1>
<br>
<p class="texto">Como visto, as lambidas de cachorro no rosto nunca serão 100% seguras, de modo que o melhor é ter um animal saudável em casa. Veja algumas medidas que podem contribuir para diminuir os riscos dos “lambeijos”:</p>
<li class="lista">
mantenha a vacinação e a vermifugação do seu pet sempre em dia;
</li>
<li class="lista">
ao passear com o cachorro, não permita que ele se aproxime de lixeiras, fezes de animais, entre outros;
</li>
<li class="lista">
ao retornar do passeio com o pet, limpe suas patas, barriga, boca e bumbum com lenços umedecidos antissépticos próprios para animais;
</li>
<li class="lista">
leve o cachorro para consultas regulares no veterinário, a fim de verificar o estado de saúde dele e identificar precocemente qualquer doença.
</li>
