    <h2>Como cuidar de uma tartaruga em casa?</h2>

    <p class="texto">Até aqui você viu alguns cuidados com tartaruga doméstica, especialmente em relação à alimentação. Mas não é só isso que importa na hora de adotar um animal dessa espécie.
<br><br>
    Apesar de parecer um pet preguiçoso, bem diferente dos agitados cães e gatos, as tartarugas precisam de um espaço só delas. Sempre considere o tamanho máximo que a espécie que você escolheu vai atingir. 
<br><br>
    Em tanques ou aquários, o ideal é que o tutor reserve cerca de um terço de área terrestre e o restante para natação, sempre com iluminação ultravioleta de espectro total. Isso faz com que o local fique em uma temperatura agradável para o pet.
<br><br>
    Lembre-se também de sempre fazer a manutenção do local. A água limpa impede a proliferação de fungos e microrganismos, preserva a saúde do animal e, claro, faz com que a tartaruga doméstica reconheça aquele como seu ambiente favorito.
