  <h3>Qual é a diferença entre tartaruga, cágado e jabuti?</h3>

    <p class="texto">
    Antes de mais nada, precisamos te contar que existem três espécies que são comumente confundidas, mas que são muito diferentes entre si: tartaruga, cágado e jabuti. Mas quais são as principais diferenças e quais desses bichos costumam ser domesticados? 
    <br><br>
    A tartaruga, por exemplo, se diferencia principalmente por conta do casco: ele é redondo, alto e duro. Já o pescoço é o mais curto dentre essas três espécies. Na natureza, ela gosta de passar mais tempo na água e se desenvolve bem no quesito nado. 
    <br><br>
    Uma das curiosidades sobre tartarugas domésticas é que elas costumam sair da água apenas para tomar sol ou desovar. Outro ponto é que um desses animais de estimação chegam a viver 40 anos com o tutor. 
    <br><br>
    Já o cágado, em seu habitat natural, vive bem tanto na água quanto na terra. Diferentemente da tartaruga doméstica, o casco tem formato achatado e mais oval, e o pescoço alongado. Não podemos esquecer das patas: elas possuem membranas entre os dedos para facilitar o nado, o que não existe nas tartarugas domésticas.
    <br><br>
    Por fim, o jabuti é o que mais se distancia em aparência da tartaruga. O casco dessa espécie é muito mais alto e mais pesado do que o das suas colegas tartaruga e cágado. Sua alimentação é essencialmente herbívora, e esse animal vive apenas em terra.</p>

    <br>
    
    <h3>Quantas e quais espécies de tartaruga existem?</h3>

    <p class="texto">Existem diversos tipos de tartarugas na natureza, mas não são todos que podemos criar em casa. Para entender melhor quais são as espécies de tartaruga doméstica, confira quatro tipos desse animal que você pode ter de estimação, sem problemas com o IBAMA.</p>

    <img src="../Imagens/tartub.jpg" alt="Tartaruga 3">

    <h4>Tartaruga Pintada</h4>

    <p class="texto">
    Animal pequeno, de cerca de 25 centímetros e que é considerado um cágado, embora tenha nome de tartaruga doméstica. Seguindo as características, o casco é rígido e com riscos amarelos. 
    <br><br>
    Por conta do tamanho, a alimentação precisa ser balanceada para evitar o sobrepeso. O ideal é oferecer rações balanceadas e próprias para tartaruga doméstica.
    </p>

    <h4>Tartaruga-da-madeira</h4>

    <p class="texto">
    Seu físico é bem colorido: o casco é cinza e o restante do corpo tem uma cor alaranjada e manchas escuras na região da cabeça e patas. Seu tamanho também é pequeno e a espécie mede em torno de 20 centímetros.
    <br><br>
    Pelo tamanho, é outro tipo de tartaruga doméstica que exige alimentação saudável e equilibrada. Nesse caso, o ideal é oferecer um misto de ração e frutas. 
    </p>

    <h4>Tartaruga-Russa</h4>

    <p class="texto">
    Essa espécie é diferente das demais por ter a extensão do corpo mais clara. O casco, no entanto, é escuro com manchas, formando um contraste bem interessante e bonito.
    <br><br>
    Também mede por volta dos 20 centímetros e costuma se alimentar de plantas. Essa é, portanto, uma das espécies mais tranquilas para se manter sem se preocupar com o sobrepeso.
    </p>

    <h4>Tartaruga-Tigre-d'água</h4>
    
    <p class="texto">
    Essa é a última das melhores espécies de tartaruga para se ter em casa. É um dos tipos que se destaca mais porque tem aparência delicada e é muito pequeno quando filhote, medindo cerca de 5 centímetros.

    Mas não se engane: ele não fica pequeno para sempre. Quando chega à fase adulta, o animal chega aos 30 centímetros. Outro ponto de destaque é a longevidade. Essa tartaruga doméstica chega a viver, em média, 30 ano
    </p>

    <h2>O que come uma tartaruga doméstica?</h2>

    <p class="texto">
    Muita gente se questiona sobre a <strong>alimentação de tartarugas domésticas</strong>. Como você viu nas espécies citadas acima, existem as que comem plantas, outras que se alimentam de frutas e outras, ainda, de ração balanceada específica.

    As refeições desses animais podem ser muito variadas, mas se você não sabe o que oferecer, o melhor é procurar um profissional para te orientar. Isso porque a ingestão de alimentos indevidos pode levar à morte do seu pet. 

    Por isso, consulte um veterinário de confiança para que ele identifique a espécie e prescreva a alimentação correta. Nesse ponto, vale lembrar, ainda, sobre as quantidades para evitar o sobrepeso. Mas os variados tipos de tartarugas podem comer alimentos como:
    </p>
