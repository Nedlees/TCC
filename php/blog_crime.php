  <h1>Crimes contra cães e gatos podem ter multa mínima de R$ 10 mil; Câmara debate</h1>

  <div class="container-blog">

    <img src="https://www.petz.com.br/blog/wp-content/uploads/2019/05/centro-de-zoonoses-pet.jpg"
      alt="">

    <p class="texto">
      O projeto de lei que eleva para R$ 10 mil a multa mínima em caso de abusos ou maus-tratos envolvendo animais,
      domésticos ou não, foi aprovado pela Comissão de Finanças e Tributação da Câmara dos Deputados.
      <br><br>
      O deputado Marcelo Queiroz (PP-RJ), relator do projeto, recomendou a aprovação do substitutivo da Comissão de Meio
      Ambiente e Desenvolvimento Sustentável para o Projeto de Lei 300/23, do deputado Célio Studart (PSD-CE).
      <br><br>
      O texto aprovado altera a Lei dos Crimes Ambientais, mas não muda o valor máximo das multas, que atualmente pode
      chegar a R$ 50 milhões. “O novo texto não altera em nada a intenção original do projeto”, disse Queiroz.
    </p>

    <h3>Lei Sansão</h3>

    <p class="texto">
      De acordo com o deputado Célio Studart, a Lei Sansão definiu em 2020 que os crimes contra cães e gatos serão
      punidos com prisão de dois a cinco anos, mais proibição da guarda e multa (sem especificar o valor).
      <br><br>
      “Dependendo dos critérios utilizados para a condenação, [os valores mínimos previstos na lei] podem ser
      considerados reduzidos, dada a reprovabilidade que tem na sociedade a conduta de maus-tratos aos animais
      domésticos”, afirmou Studart.
      <br><br>
      Para o deputado, o valor da multa deve ser capaz de constituir punição justa para os casos de maus-tratos a cães e
      gatos, para que não haja reincidência. O projeto será analisado pela Comissão de Constituição e Justiça e de
      Cidadania. Para virar lei, também terá de ser aprovado pelo Senado.
    </p>

