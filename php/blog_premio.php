  <h1>Brasileira recebe prêmio internacional por projeto que substitui testes em animais na indústria de cosméticos</h1>

  <div class="container-blog">

    <img src="https://jornalpanoramaminas.com.br/site/wp-content/uploads/2024/07/1-21.jpeg" alt="">
    <br>
    <p class="texto">
      A pesquisadora brasileira Lauren Dalat de Souza Coelho, de 25 anos, esteve em Londres, em junho, para receber o
      prêmio Lush Prize 2024. Ao todo, foram agraciados 14 projetos de nove países.
      <br><br>
      Ela foi uma das cinco vencedoras da categoria Jovem Pesquisador e ganhou 10 mil libras esterlinas (mais de R$ 70
      mil), que vai investir na continuidade de sua pesquisa, visto que o modelo de testagem que propõe ainda não está
      pronto para ser aplicado na indústria.
      <br><br>
      O Lush Prize é o maior fundo global no setor de testes em animais, que oferece 250 mil libras esterlinas (mais de
      R$ 1,7 bilhão) para apoia iniciativas que substituem ou acabam com esse tipo de testes, abrangendo cinco
      categorias principais: Ciência, Educação, Conscientização Pública, Lobbying e Jovem Pesquisador/a. E, desde 2012,
      ainda destaca três reconhecimentos especiais: o Prêmio Andrew Tyler(para contribuições excepcionais para a
      erradicação dos testes), Colaboração Científica Relevante e Realização Política.
      <br><br>
      O Lush Prize deste ano integra a campanha lançada pela empresa inglesa Lush (que produz cosméticos frescos feitos
      à mão), Fighting Animal Testing (Lutando Contra a Experimentação Animal, em tradução livre). O troféu tem o
      formato de um coelho, um dos animais mais utilizados em testes nos laboratórios.
    </p>

    <h3>Células-tronco dentárias</h3>

    <p class="texto">
      Bacharel em Farmácia e mestranda do Programa de Pós-Graduação em Ciências Farmacêuticas da Universidade Federal de
      Goiás (UFG), Lauren apresentou projeto que substitui testes em animais na indústria de cosméticos com um modelo de
      testagem baseado em células-tronco extraídas de dentes.
      <br><br>
      Hoje, a testagem é feita em animais ou em células extraídas de animais, mas, de acordo com Lauren, esses
      experimentos podem não ser tão precisos para identificar a reação em humanos.
      <br><br>
      De acordo com a pesquisa da brasileira, células-tronco dentárias podem ser usadas para identificar substâncias em
      cosméticos que podem causar malformações congênitas em bebês. Ao Lush Prize, ela sugeriu uma plataforma para
      testar substâncias em cosméticos usando modelo que combina células-tronco dentárias e pele humana 3D. E levou o
      prêmio!
      <br><br>
      “Não acreditei quando fui pré-selecionada e fiquei ainda mais feliz quando recebi esse reconhecimento, que é
      importante para reforçar a necessidade de criação de novas metodologias que não utilizem animais”, declarou ao G1.
    </p>

    <h3>Método mais confiável e ético</h3>

    <p class="texto">
      Lauren faz parte de um grupo de pesquisa dedicado ao estudo de alternativas para testes com animais, que recebe
      orientação da professora Marize Campos Valadares, no Laboratório de Ensino e Pesquisa em Toxicologia In Vitro da
      Faculdade de Farmácia da UFG (Tox In-FF/UFG).
      <br><br>
      A partir desse trabalho, cada participante desenvolveu estudo na área.
      <br><br>
      “Sempre gostei muito de animais e atuei em diversas causas, então consegui unir o útil ao agradável. Uma colega
      sempre diz que consegui juntar o amor pelos animais com a pesquisa, que também é algo de que eu sempre gostei”,
      contou.
      <br><br>
      Segundo ela, a testagem com células-tronco dentárias se utiliza de um método mais confiável e ético para testar
      cosméticos, o que garante a segurança dos consumidoressem que se use animais
      <br><br>
      “Além de ser eticamente questionável, o principal problema da metodologia [que testa produtos em animais] é o fato
      de que pode haver variação entre as espécies, ou seja, a resposta do animal pode não ser igual à resposta humana”,
      destaca a brasileira. “É uma técnica cara e trabalhosa e utiliza um grande número de animais por experimento”.
    </p>

    <h3>Testagem mais realista e sem animais</h3>

    <p class="texto">
      Promulgada em 23 de fevereiro de 2023, a Resolução nº 58, do CONCEA – Conselho Nacional de Controle de
      Experimentação Animal, proibiu o “uso de animais vertebrados em pesquisa científica, desenvolvimento e controle de
      produtos de higiene pessoal, cosméticos e perfumes que utilizem em suas formulações ingredientes ou compostos com
      segurança e eficácia já comprovadas cientificamente”. Por isso, a busca por alternativas para as indústrias tem
      sido incessante.
      <br><br>
      “Acredito que, aqui no laboratório, abordamos duas questões: o aspecto ético, porque não faz sentido submeter um
      animal a um ensaio com baixa predição, e a obtenção de respostas mais fidedignas usando linhagens celulares e
      cultivos primários de células humanas, em vez de animais”.
      <br><br>
      Lauren explica que os dentes e a pele utilizados em seu modelo de testagem podem ser obtidos por meio de doações
      voluntárias, de doadores saudáveis de pessoas de 5 a 30 anos. “A pele, por exemplo, pode ser doada após remoção
      cirúrgica do excesso de pele”.
      <br><br>
      A pesquisadora brasileira ainda lembrou ao G1 sobre a tragédia ocorrida em meados da década de 1960, devido ao uso
      de talidomida – medicamento amplamente prescrito para combater náuseas em mulheres grávidas em todo o mundo –, que
      provocou grande número de malformações congênitas.
      <br><br>
      Ela ainda destacou que ensaios pré-clínicos em animais não mostraram efeitos teratogênicos do remédio, ou seja,
      capacidade de causar malformações no feto.
      <br><br>
      “No entanto, uma vez metabolizada no corpo humano, ocorria a formação do isômero S, que apresentava essa
      propriedade teratogênica. Isso resultou nos casos de focomelia [anomalia congênita que impede a formação normal de
      braços e pernas] em bebês expostos à talidomida”.
      <br><br>
      Por isso, a pesquisadora defende a testagem em tecido de pele humana 3D com derivados de células-tronco dentárias,
      já que ela permite avaliar os efeitos de forma mais realista.
      <br><br>
      E ela destaca: a metodologia padrão atual para detectar o potencial teratogênico utiliza mil animais por
      experimento, em média. , com seu modelo, é possível cultivar várias garrafas de células-tronco a partir de uma
      única amostra.
      <br><br>
      “A partir de um dente, é possível extrair células-tronco da papila, do ligamento e da polpa. Como extraímos essas
      células dos explantes (fragmentos do tecido), é possível cultivar várias garrafas a partir de uma única amostra”,
      explicou a brasileira ao G1.
    </p>

