<h1>Por que gatos 'amassam pãozinho'? Entenda o comportamento do seu felino.</h1></center>
<div class="alinhar">
<p>Quem tem um felino em casa provavelmente já viu seu gato amassando pãozinho. Se você está por fora do assunto, a gente explica: “amassar pãozinho” é o termo utilizado para se referir à mania de afofar as superfícies, sejam elas objetos ou pessoas.
Esse comportamento começa desde cedo e pode continuar até a fase idosa, mas, apesar de ser extremamente comum, muitas pessoas não sabem o que ele significa. Por isso, a gente veio explicar o porquê de os felinos amassarem pãozinho. Descubra, a seguir, e entenda seu gato!</p>
<br>
</div>
<br>
<h2>1.Demonstrar felicidade</h2>
<p class="pp">Como mencionamos, quando vemos um gato amassando pãozinho é sinal de que ele está relaxado e bem ao seu lado e no ambiente. Uma prova disso é que, na maioria das vezes, os gatos amassam pãozinho e ronronam ao mesmo tempo.</p>
<br>
<h2>2.Tornar o local mais confortável</h2>
<p>Além de pessoas, é normal ver o gato afofando camas, cobertores e travesseiros. Isso acontece quando ele está se preparando para dormir e quer deixar o local o mais confortável possível para descansar.
 Os felinos dormem por muito tempo e, por isso, se dedicam bastante na hora de afofar a cama. Afinal, nada melhor do que tirar um cochilo em um lugar macio e aconchegante, não é mesmo?</p>
<br>
<h2>3.Marcar território</h2>
<p>A ação de amassar pãozinho ativa glândulas nas patas que liberam um odor característico do pet. Como os gatos são territorialistas, tal comportamento também pode ser entendido como uma tentativa de marcar território. Ao afofar o colo, o felino deixa claro para outros animais que seus humanos favoritos já têm dono.</p>
<br>
<h2>4. Alongar o corpo</h2> 
<p>Por fim, o hábito de amassar pãozinho também acontece quando o gato precisa se alongar. Dessa forma, ele consegue estimular os músculos e a circulação sanguínea.</p>

<h1>Meu gato não amassa pãozinho. Devo me preocupar?</h1>

<p class="alinhar2">Não, está tudo bem! Da mesma forma que alguns gatos amassam pãozinho sempre, outros podem passar a vida toda sem desenvolver esse comportamento. Mas não precisa se preocupar: existem diversas formas de demonstrar felicidade e apego aos tutores e tutoras. 
Se seu gato não amassa pãozinho, ele pode ronronar, pedir para brincar ou simplesmente se sentar ao seu lado. Cada animal é único e tem jeitos próprios de se expressar.
Agora, se seu pet costumava amassar pãozinho e parou de uma hora para outra, é preciso prestar atenção: ele pode estar estressado ou incomodado com alguma situação. Felinos são bem adaptados à rotina e, por isso, qualquer novidade pode causar desconforto, como a troca de ração, mudança de casa ou chegada de um novo membro na família. Nesse caso, é importante consultar um veterinário e investigar a fonte do problema o mais rápido possível.</p>

