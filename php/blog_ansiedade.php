    <h2>1.Ansiedade em cachorros: por que acontece?</h2>
    <p class="pp">Todo tutor faz o que acredita ser o melhor para o amigo de quatro patas. Por isso, é difícil
      reconhecer quando certas atitudes afetam negativamente a vida do cachorro. Porém, repensar e modificar nosso
      comportamento, buscando restabelecer o bem-estar do pet, são passos fundamentais.

      O cachorro com ansiedade de separação tem um vínculo tão grande com o tutor, que acaba tornando-se uma
      dependência. Ao não ser incentivado a fazer coisas sozinho, o peludo se sente perdido e tem medo de ficar longe,
      quando o tutor passa o dia fora trabalhando, por exemplo. <br><br>Essa situação pode se tornar uma síndrome do
      abandono. O cachorro com crise de ansiedade sente-se ainda mais frágil e sozinho, desenvolvendo outros problemas
      de saúde, como depressão, gastrite, crises de vômitos e diminuição da imunidade. </p>
    <br>
    <h2>2.Outros casos</h2>
    <p class="pp">O cachorro ansioso à noite pode ser um sinal de medo de dormir devido a episódios anteriores
      relacionados a situações de abandono, tortura, agressão e traumas da criação anterior. Além disso, o estresse pode
      ser a causa desse distúrbio.

      O cachorro ansioso para comer devora os grãos de ração em pouco tempo, permanecendo agitado e esperando por mais.
      O comportamento agrava-se quando o cãozinho fica agressivo e tenta pegar comida humana ou de outros pets da casa.
    </p>
    <br>

    <h2>3.Como diminuir a ansiedade nos peludos?</h2>
    <p class="pp">No geral, para diminuir a ansiedade dos pets, é preciso que eles tenham confiança e sejam preparados
      para serem independentes. Ao seguirem uma rotina, eles já estarão acostumados com os momentos em que estarão
      sozinhos e com o tutor, por exemplo.
      <br>
      Os passeios diários também são ótimos para reduzir o estresse e a ansiedade. Crie o hábito de levá-lo para dar uma
      voltinha na rua, gastar as energias e interagir com outros cachorros. Esse momento do dia pode ser feito várias
      vezes, quanto você achar necessário.
      <br>
      Quando o caso é mais específico, como a ansiedade de cachorro por separação, é frequente a recomendação para não
      celebrar muito ao chegar e sair de casa, pois isso pode deixá-lo mais ansioso. Quando for possível, deixe itens
      que distraiam seu pet na sua ausência.
      <br>
      Faça o teste para ver como o cachorro com ansiedade comporta-se e com quais soluções ele está se adaptando.
      Trabalhe desde cedo a autonomia do seu amigo de quatro patas e estimule-o a tentar fazer as coisas sozinho, como
      alimentar-se e brincar.
    </p>
    <br>
    <h2>4.Tratamento medicamentoso</h2>
    <p class="pp">Esteja atento para as melhorias na ansiedade do seu companheiro. Se notar que ainda não houve mudança,
      consulte um médico-veterinário para entender quais podem ser os tratamentos alternativos e o que pode ser feito.

      Em quadros mais graves, é provável que o veterinário indique medicamentos, como calmante para o cachorro.
      Atualmente, inclusive, já existem opções fitoterápicas menos fortes para o organismo, mas ainda eficazes.

      Lembre-se: para não colocar seu companheiro em risco, não inicie tratamentos com medicação sem a orientação
      médica. </p>

