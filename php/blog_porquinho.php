    <p class="texto">Conhecido também como Coroado Inglês, eles ganharam esse majestoso nome porque possuem um tipo de coroa no topo
      da cabeça. Suas cores costumam ser branco, preto e caramelo, sendo uma das raças mais comuns em território
      brasileiro.</p>

    <h4>Porquinho-da-Índia Teddy</h4>

    <img src="https://www.petz.com.br/blog/wp-content/uploads/2020/01/porquinho-da-india-teddy.jpg" alt="">

    <p class="texto">Com uma pelagem áspera e eriçada, o também chamado de porquinho-da-Índia Teddy Americano, tem uma aparência muito
      fofa, sendo comparado com um ursinho de pelúcia. Com origem nos Estados Unidos, é possível encontrá-lo com uma
      tonalidade predominante mais clara com manchas pretas e marrons pelo corpo.</p>

    <h4>Porquinho-da-Índia Abissínio</h4>

    <img src="https://www.petz.com.br/blog/wp-content/uploads/2020/01/porquinho-da-india-abissinio.jpg" alt="">

    <p class="texto">No meio-termo entre pelo curto e longo está o porquinho-da-Índia Abissínio. Sua pelagem média dá um ar bagunçado
      a esse pequeno roedor, deixando-o cheio de charme!</p>

    <h4>Porquinho-da-Índia Peruano</h4>

    <img src="https://www.petz.com.br/blog/wp-content/uploads/2020/01/porquinho-da-india-peruano.jpg" alt="">

    <p class="texto">O porquinho-da-Índia Peruano é um dos preferidos quando o assunto é pelo longo. Suas madeixas caem sobre o seu
      pequeno corpinho, o que acaba exigindo cuidados específicos, como realizar escovações diárias.</p>

    <h4>Porquinho-da-Índia Alpaca</h4>

