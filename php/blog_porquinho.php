    <p class="texto">Este pet possui a pela frisada, bem diferente dos seus primos de pelos lisos. A pelagem longa facilita a
      proliferação de micro-organismos na sua pelagem. Então, atenção redobrada para cuidar do seu amigo e evitar o
      contato do animal com umidade.
      <br><br>
      Outras raças de porquinhos-da-Índia de pelagem longa são: Angorá e Sheltie.
