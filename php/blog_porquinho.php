    <h3>Qual a origem do Porquinho-da-Índia?</h3>

    <p class="texto">Por mais que seu nome seja porquinho, esse pequeno pet não é da família dos suínos. Na verdade, ele é um roedor
      da
      família Caviidae. Além disso, também se engana quem pensa que ele veio do país da Ásia Meridional – na verdade,
      esse
      animal é sul-americano.
      <br><br>
      O porquinho de estimação é originário dos Andes, na América do Sul. Ele descende de uma espécie de preá, que era
      usado na alimentação de povos indígenas. Com o tempo, no entanto, esse animal saiu do cardápio dos andinos e foi
      para os lares de todo o mundo, por seu temperamento amigável e os cuidados fáceis.
      <br><br>
      Entre os nomes como é conhecido esse pet, estão:
    </p>
    <li class="lista">Porco-da-Índia;</li>
    <li class="lista">Preá;</li>
    <li class="lista">Cuy;</li>
    <li class="lista">Cobaia;</li>
    <li class="lista">Sauiá-Cobaia;</li>
    <li class="lista">Preá-do-Reino;</li>
    <li class="lista">Coelho-da-Índia.</li>

    <h3>Quais são as características de um porquinho-da-Índia?</h3>

    <p class="texto">Parentes próximos de roedores como a capivara e o preá, os porquinhos-da-Índia podem medir entre 20 e 25
      centímetros e pesam cerca de 1kg. Com um corpo arredondado e compacto, esses bichinhos têm pequenas orelhas
      arredondadas.
      <br><br>
      As suas cores variam de acordo com a raça e por causa do processo de domesticação do pet. A grande variedade de
      tonalidade acontece pela quantidade de cruzamento que ocorrem na espécie. Então, você pode encontrar por aí
      porquinho-da-Índia nas cores:
    </p>
    <li class="lista">Branco;</li>
    <li class="lista">Preto;</li>
    <li class="lista">Marrom;</li>
    <li class="lista">Albinos;</li>
    <li class="lista">Bicolores;</li>
    <li class="lista">Amarelados;</li>
    <li class="lista">Entre outros Tons.</li>

    <h3>Porquinho-da-Índia: raças</h3>

    <p class="texto">Você sabia que as raças de porquinho-da-Índia são diferenciadas principalmente pela pelagem? Sim, poucas pessoas
      sabem, mas existem variadas espécies desse grupo de roedores. Conheça mais sobre as raças famosas e as
      características de cada um. Confira!</p>

    <h4>Porquinho-da-Índia Ridgeback</h4>

    <img src="https://www.petz.com.br/blog/wp-content/uploads/2020/01/porquinho-da-india-ridgeback.jpg" alt="">

    <p class="texto">Com pelagem que varia entre as cores branco, preto e marrom, esse fofo bichinho tem linhas de pelos eriçados nas
      costas, muito semelhante ao formato de penteado estilo moicano. Essa é uma espécie difícil de ser encontrada no
      Brasil.</p>

    <h4>Porquinho-da-Índia Inglês</h4>
    <div class="divisao">
      <img src="https://www.petz.com.br/blog/wp-content/uploads/2020/01/porquinho-da-india-ingles.jpg" alt="">
      <img src="https://www.petz.com.br/blog/wp-content/uploads/2020/01/porquinho-da-india-coroado.jpg" alt="">
